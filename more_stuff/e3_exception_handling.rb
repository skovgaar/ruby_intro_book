# What is exception handling and what problem does it solve?

=begin
Programs will run into errors especially if we deal with
data from the outside fx a user input. So we need a way 
to handle these errors. Exception handling can help rescue
these errors so the program doesnt exit in a bad spot, but 
can continue to work and possibly give the user a sensible
message about the error.. And maybe report the real error
back to the developer?
=end

# Solution
# Exception handling is a structure used to handle the possibility of an error occurring in a program. It is a way of handling the error by changing the flow of control without exiting the program entirely.
